// for video 1
var hoverDiv = document.getElementById('video1');
var targetDiv = document.getElementById('center-text');

hoverDiv.addEventListener('mouseover', function() {
  targetDiv.classList.add('hide');
  setTimeout(function() {
    targetDiv.textContent = 'This is video 1';
    targetDiv.classList.remove('hide');
    targetDiv.classList.add('show');
    targetDiv.style.zIndex=1;
  }, 300);
});

hoverDiv.addEventListener('mouseout', function() {
  targetDiv.classList.remove('show');
  targetDiv.classList.add('hide');
  setTimeout(function() {
    targetDiv.textContent = 'Interactive Studio';
    targetDiv.classList.remove('hide');
    targetDiv.style.zIndex=-1;
  }, 300);
});


